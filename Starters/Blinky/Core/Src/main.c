void vPrintSerial(const char* format, ...)
{
	va_list ap;
	va_start(ap, format);
	size_t len = vsnprintf((char*)buf, sizeof(buf), format, ap);
	va_end(ap);

	if (len > sizeof(buf) - 1)
		len = sizeof(buf) - 1;

	// Retry up to 10 times if CDC is busy
	uint8_t result;
	for (int i = 0; i < 10; i++) {
		result = CDC_Transmit_FS(buf, len);
		if (result == USBD_OK)
			break;
		if (result != USBD_BUSY)
			break;  // Error occurred, exit
		HAL_Delay(1);  // Small delay before retry
	}
}

